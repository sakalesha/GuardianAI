/**
 * Calculates the distance between two GPS coordinates in meters using the Haversine formula.
 */
export function getDistanceInMeters(lat1, lon1, lat2, lon2) {
  const R = 6371e3; // Earth's radius in meters
  const φ1 = (lat1 * Math.PI) / 180;
  const φ2 = (lat2 * Math.PI) / 180;
  const Δφ = ((lat2 - lat1) * Math.PI) / 180;
  const Δλ = ((lon2 - lon1) * Math.PI) / 180;

  const a =
    Math.sin(Δφ / 2) * Math.sin(Δφ / 2) +
    Math.cos(φ1) * Math.cos(φ2) * Math.sin(Δλ / 2) * Math.sin(Δλ / 2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));

  return R * c;
}

/**
 * Checks if the resolution photo is within the allowed threshold (50m) of the original complaint.
 */
export function isLocationMatch(lat1, lon1, lat2, lon2, threshold = 50) {
  const distance = getDistanceInMeters(lat1, lon1, lat2, lon2);
  return distance <= threshold;
}
